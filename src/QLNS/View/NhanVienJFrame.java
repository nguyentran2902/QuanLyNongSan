    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (isCorrect()) {
            NhanVien nv = new NhanVien();
            nv.setIdNV(idNV);
            nv.setTenNV(txtNameNV.getText());
            nv.setDiachiNV(txtAddress.getText());
            nv.setSdtNV(txtSdt.getText());
            nv.setBoPhan(cbbBophan.getSelectedItem() + "");
            Boolean gioitinh = true;
            if (rbtnNu.isSelected()) {
                gioitinh = false;
            }
            nv.setGioitinhNV(gioitinh);

            nv.setNgaySinh(txtNgaysinh.getDate());

            nv.setTaiKhoan(txtUser.getText());
            nv.setMatKhau(txtPass.getText());
            Boolean trangthai = true;
            if (!jcbTinhtrang.isSelected()) {
                trangthai = false;
            }
            nv.setTrangThai(trangthai);

            int confirm = JOptionPane.showConfirmDialog(rootPane,
                    "Bạn có chắc chắn muốn sửa thông tin nhân viên này?",
                    "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                try {
                    if (nvService.editNhanvien(nv)) {
                        JOptionPane.showMessageDialog(this,
                                "Sửa thông tin nhân viên thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        refreshListNV();

                    } else {
                        JOptionPane.showMessageDialog(this,
                                "Sửa thông tin không thành công", "Lỗi", JOptionPane.ERROR_MESSAGE);
                    }

                } catch (SQLException ex) {
                    ex.printStackTrace();
                }

            }

        }
    }//GEN-LAST:event_btnEditActionPerformed
