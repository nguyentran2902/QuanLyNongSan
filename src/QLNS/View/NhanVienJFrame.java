    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên muốn xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa nhân viên này?",
                    "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                String idNV = String.valueOf(tblNhanVien.getValueAt(row, 0));
                try {
                    if (nvService.existNhanVienInHD(idNV)) {
                        JOptionPane.showMessageDialog(NhanVienJFrame.this, "Không thể xóa nhân viên này \n"
                                + "Nhân viên còn tồn tại trong danh mục hóa đơn",
                                "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } else {
                        if (nvService.deleteNhanvien(idNV)) {
                            JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                            refreshListNV();

                        } else {
                            JOptionPane.showMessageDialog(this,
                                    "Xóa nhân viên không thành công", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        }
                    }

                } catch (SQLException ex) {
                    ex.printStackTrace();
                }

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

