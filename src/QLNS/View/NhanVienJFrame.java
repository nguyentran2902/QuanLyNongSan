    //render Bang NhanVien
    private void setTableData(List<NhanVien> ListNv) {
        for (NhanVien nv : ListNv) {

            //set gioi tinh
            String gioitinh = "Nam";
            if (nv.getGioitinhNV() == false) {
                gioitinh = "Nữ";
            }
            //set ngaysinh
            String ngaysinh = null;
            DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
            if (nv.getNgaySinh() != null) {
                ngaysinh = df.format(nv.getNgaySinh());

            }

            defaultTableModel.addRow(new Object[]{nv.getIdNV(), nv.getTenNV(),
                nv.getBoPhan(), nv.getDiachiNV(), nv.getSdtNV(), gioitinh,
                ngaysinh, nv.getTrangThai()});
        }
    }

    //render NhanVien ra form
    private void setRowData(int row) throws SQLException {

        String MaNV = tblNhanVien.getValueAt(row, 0) + "";
        NhanVien nv = nvService.getNvbyId(MaNV);

        idNV = Integer.valueOf(MaNV);
        txtNameNV.setText(nv.getTenNV());
        txtAddress.setText(nv.getDiachiNV());
        cbbBophan.setSelectedItem(nv.getBoPhan());
        txtSdt.setText(nv.getSdtNV());
        if (nv.getGioitinhNV() == true) {
            rbtnNam.setSelected(true);
        } else {
            rbtnNu.setSelected(true);
        }

        txtNgaysinh.setDate(nv.getNgaySinh());
        txtUser.setText(nv.getTaiKhoan());
        txtPass.setText(nv.getMatKhau());
        if (nv.getTrangThai() == true) {
            jcbTinhtrang.setSelected(true);
        } else {
            jcbTinhtrang.setSelected(false);
        }
    }

