    //Xóa khách hàng
    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblKhachHang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng muốn xóa", "Lỗi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(rootPane, "Bạn có chắc chắn muốn xóa khách hàng này?",
                    "Thông báo", JOptionPane.YES_NO_CANCEL_OPTION);

            if (confirm == JOptionPane.YES_OPTION) {
                String maThe = String.valueOf(tblKhachHang.getValueAt(row, 1));
                try {

                    if (khService.existKhachHangInHD(maThe)) {
                        JOptionPane.showMessageDialog(KhachHangJFrame.this, "Không thể xóa khách hàng này \n"
                                + "Khách hàng còn tồn tại trong danh mục hóa đơn",
                                "Thông báo", JOptionPane.ERROR_MESSAGE);
                    } else {
                        if (khService.deleteKhachHang(maThe)) {
                            refreshListKH();

                            JOptionPane.showMessageDialog(this, "Xóa thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        } else {
                            JOptionPane.showMessageDialog(this,
                                    "Xóa khách hàng không thành công", "Lỗi", JOptionPane.ERROR_MESSAGE);
                        }
                    }
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }

            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

