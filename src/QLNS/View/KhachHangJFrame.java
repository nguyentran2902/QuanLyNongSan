    private void setRowData(int row) throws SQLException {
        if (tblKhachHang.getRowCount() > 0) {
            KhachHang kh = khService.getKhbyMaThe(tblKhachHang.getValueAt(row, 1) + "");

            txtMaThe.setText(tblKhachHang.getValueAt(row, 1) + "");
            txtTenKH.setText(tblKhachHang.getValueAt(row, 2) + "");
            txtCCCD.setText(tblKhachHang.getValueAt(row, 3) + "");
            txtAddress.setText(tblKhachHang.getValueAt(row, 4) + "");
            txtSdtKh.setText(tblKhachHang.getValueAt(row, 5) + "");
            txtNgaytao.setText(sdf.format(kh.getNgaytao()));
            txtTichLuy.setText(currencyVN.format(kh.getTichLuy()));

        }
    }

    //render Bang KhachHang
    private void setTableData(List<KhachHang> ListKh) {
        defaultTableModel.setRowCount(0);
        for (int i = 0; i < ListKh.size(); i++) {
            defaultTableModel.addRow(new Object[]{i + 1, ListKh.get(i).getMaThe(), ListKh.get(i).getTenKH(),
                ListKh.get(i).getCCCD(), ListKh.get(i).getDiachiKH(), ListKh.get(i).getSdtKH()});
        }
    }
